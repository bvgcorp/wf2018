	                    <h3 class="title">BYOC？ Lan Party？ WirForce？</h3>
                        <p>BYOC？ Lan Party？ WirForce？ 多麼陌生的名詞呀！</p>
                        <p>這裡請容我們簡單為你介紹這些名詞所代表的意義！</p>
                        <img src="img/line_item.png" class="line-item">
                        
                        <h3 class="title">什麼是 BYOC？</h3>
                        <p>「 BYOC 」是 Bring Your Own Computer 的縮寫，意指自行攜帶電腦。WirForce 正是一場 BYOC 形式的社群盛會！</p>
						<p>今年的 WirForce 將有 1,200 席以上的 BYOC 名額，報名從 9 月 3 日起到額滿為止，記得趕緊報名！也別忘了到時候要帶齊你的傢伙來現場同歡。</p>
						<img src="img/line_item.png" class="line-item">
                        
                        <h3 class="title">什麼是 Lan Party ?</h3>
						<p>Lan Party （網路派對）指的是由一群人暫時性、通常也是自發性的帶著他們的硬體配備與電腦 (BYOC)，在一個地方以區域網路連結的聚會，這樣的聚會大部份是以玩多人網路遊戲為目的，這樣的區域網路可大可小，從只有兩三個人到數千人都有。</p>
						<p>目前世界上最大型的 Lan Party 就是瑞典的「 DreamHack 」，從原先的數百人規模到 2002 年開始壯大，每年分為夏、冬兩季，每季參與人數高達 3,000 至 6,000 人，至 2009 年起，其規模已超過 20,000 名參加者。</p>
						<img src="img/line_item.png" class="line-item">
						
						<h3 class="title">什麼是 WirForce？</h3>
<p>臺灣本土也有自己的 Lan Party 活動，就是 WirForce！</p>
<p>WirForce 於 2014 年首度舉辦時採用邀請制並不對外開放，包含各界知名實況主、名人與選手等遊戲相關人士約 200 位，各自攜帶個人電腦、筆電、配備齊聚現場。會場內除打遊戲外，參加者可以進行各種合法活動；現場亦有許多線下趣味活動與節目，內容包羅萬象精彩萬分。透過全程轉播，2014 年 WirForce 結束後立即在遊戲與電競玩家中造成轟動，並成為臺灣遊戲電競史上無人可取代的黃金傳說，說是臺灣電競遊戲界春吶一點也不為過。</p>
<p>許多人為錯過此 2014 盛事感到極度遺憾與扼腕，於是 2015 年的 WirForce 決定開放場外玩家自由購票報名入場後，400 張 BYOC 座位於兩天內即銷售一空，活動三天中超過兩萬名玩家與二十家國內外知名廠商一起共襄盛舉；2016年 WirForce BYOC 座位擴增至 600 席，吸引 20 家知名品牌熱列支持，在臺北市花博公園爭艷館 C2 展區舉行，免費入場的人潮上看 7 萬、線上直播觀看人次高達 100 萬。四天三夜延續不斷電的電玩派對外，電競、派對、音樂、社群、實況、改裝、最新硬體及遊戲；Wirforce 2017更是將電玩派對的熱忱推上另一個高峰，在1000多席BYOC座位搶購一空之餘，2017年免費進場人數更是達10萬人次，線上直播也刷新直逼120萬人觀看數。</p>
<p>今年，2018 年， WirForce BYOC 座位擴增超過 1,200 席，讓我們再次一同創造亞洲有史以來最空前的電競盛會 - WirForce!</p>
<img src="img/line_item.png" class="line-item">

<h3 class="title">我可以在 WirForce 裡做什麼？</h3>
<p>如上述所言，除了瘋狂打電動外，你可以在 WirForce 會場進行各種合法活動，包含各種桌遊、競賽，在空檔之餘觀賞影片或閒聊打屁，只要你想得到的都可以作為你的活動內容，我們完全不排斥各種多角化發展，甚至還會與你一起同樂。</p>
<p>WirForce 為四天三夜不間斷的活動，歡迎各位在期間內與新舊朋友一同盡情玩樂，但也請攝取足夠的水分與熱量，更要有足夠的睡眠，請大家務必要注意自己的身體健康與人身安全。</p>
<p>若在活動期間感到任何不適，請立刻告知工作人員或前往服務台，我們會依據您的身體狀況作必要的處理。</p>
